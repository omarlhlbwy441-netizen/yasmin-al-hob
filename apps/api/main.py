@app.post("/api/auth/register")
async def register(user: UserRegister):
    if user.email in users_db:
        raise HTTPException(status_code=400, detail="البريد مستخدم بالفعل")
    user_id = str(uuid.uuid4())
    hashed_password = pwd_context.hash(user.password)
    users_db[user.email] = {
        "id": user_id,
        "email": user.email,
        "password": hashed_password,
        "first_name": user.first_name,
        "last_name": user.last_name,
        "created_at": datetime.now().isoformat()
    }
    token = create_token({"sub": user.email, "id": user_id})
    return {"token": token, "user": {"id": user_id, "email": user.email, "name": f"{user.first_name} {user.last_name}"}}

@app.post("/api/auth/login")
async def login(user: UserLogin):
    db_user = users_db.get(user.email)
    if not db_user or not pwd_context.verify(user.password, db_user["password"]):
        raise HTTPException(status_code=401, detail="بيانات الدخول غير صحيحة")
    token = create_token({"sub": user.email, "id": db_user["id"]})
    return {"token": token, "user": {"id": db_user["id"], "email": db_user["email"], "name": f"{db_user['first_name']} {db_user['last_name']}"}}


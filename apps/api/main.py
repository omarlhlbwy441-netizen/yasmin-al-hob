ALGORITHM = "HS256"

def create_token(data: dict):
    to_encode = data.copy()
    expire = datetime.utcnow() + timedelta(days=7)
    to_encode.update({"exp": expire})
    return jwt.encode(to_encode, SECRET_KEY, algorithm=ALGORITHM)

def verify_token(token: str):
    try:
        payload = jwt.decode(token, SECRET_KEY, algorithms=[ALGORITHM])
        return payload
    except:
        return None

async def get_current_user(credentials: HTTPAuthorizationCredentials = Depends(security)):
    token = credentials.credentials
    payload = verify_token(token)
    if not payload:
        raise HTTPException(status_code=401, detail="توكن غير صالح")
    user = users_db.get(payload.get("sub"))
    if not user:
        raise HTTPException(status_code=401, detail="المستخدم غير موجود")
    return user

class Agent:
    def __init__(self, name: str, role: str, icon: str, color: str):
        self.id = str(uuid.uuid4())
        self.name = name
        self.role = role
        self.icon = icon
        self.color = color
        self.status = "idle"
        self.current_task = None
        self.skills = []
        self.memory = []

    async def process(self, task: str, context: dict = None):
        self.status = "working"
        self.current_task = task
        await asyncio.sleep(1)
        result = await self.generate_code(task, context)
        self.status = "idle"
        self.current_task = None
        self.memory.append({"task": task, "result": result, "time": datetime.now().isoformat()})
        return result

    async def generate_code(self, task: str, context: dict = None):
        return {
            "agent": self.name,
            "code": f"# Generated by {self.name}\n# Task: {task}\n\nclass GeneratedProject:\n    pass",
            "files": [
                {"name": "main.py", "content": f"# {self.name} generated code"},
                {"name": "config.json", "content": "{\"version\": \"1.0.0\"}"}
            ],
            "suggestions": ["أضف اختبارات وحدة", "حسن الأداء", "أضف توثيق"]
        }


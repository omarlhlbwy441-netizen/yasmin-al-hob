@app.websocket("/ws")
async def websocket_endpoint(websocket: WebSocket):
    await websocket.accept()
    websocket_connections.append(websocket)
    try:
        await websocket.send_json({
            "type": "welcome",
            "message": "🌸 مرحباً بك في ياسمين دارك تكنولوجي!",
            "agents": [a.name for a in agents.values()],
            "programmers": len(programmers_pool)
        })
        while True:
            data = await websocket.receive_json()
            if data.get("type") == "chat":
                user_msg = data.get("message", "")
                for agent in agents.values():
                    result = await agent.process(user_msg)
                    await websocket.send_json({
                        "type": "agent_response",
                        "agent": agent.name,
                        "icon": agent.icon,
                        "color": agent.color,
                        "message": result["code"][:200] + "...",
                        "timestamp": datetime.now().isoformat()
                    })
            elif data.get("type") == "create_project":
                project = await orchestrator.create_project(
                    ProjectCreate(**data.get("project", {})),
                    data.get("user_id", "anonymous")
                )
                await websocket.send_json({"type": "project_created", "project": project})
            elif data.get("type") == "ping":
                await websocket.send_json({"type": "pong", "time": datetime.now().isoformat()})
    except WebSocketDisconnect:
        websocket_connections.remove(websocket)
    except Exception:
        if websocket in websocket_connections:
            websocket_connections.remove(websocket)


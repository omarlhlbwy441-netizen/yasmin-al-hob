class NeuralOrchestrator:
    def __init__(self):
        self.active_projects = {}
        self.task_queue = asyncio.Queue()

    async def create_project(self, project_data: ProjectCreate, user_id: str):
        project_id = str(uuid.uuid4())
        project = {
            "id": project_id,
            "name": project_data.name,
            "description": project_data.description,
            "type": project_data.project_type,
            "requirements": project_data.requirements,
            "user_id": user_id,
            "status": "planning",
            "agents": [],
            "code_files": [],
            "created_at": datetime.now().isoformat(),
            "progress": 0
        }
        await self.assign_agents(project)
        projects_db[project_id] = project
        self.active_projects[project_id] = project
        return project

    async def assign_agents(self, project: dict):
        agent_types = ["ux_designer", "architect", "developer", "qa_engineer", "devops"]
        if project["type"] == "game":
            agent_types.append("game_dev")
        if project["requirements"] and "security" in project["requirements"].lower():
            agent_types.append("security")

        for agent_type in agent_types:
            agent = agents[agent_type]
            project["agents"].append({
                "id": agent.id,
                "name": agent.name,
                "role": agent.role,
                "status": "assigned",
                "icon": agent.icon,
                "color": agent.color
            })
            asyncio.create_task(agent.process(
                f"بناء {project['type']}: {project['description']}",
                project
            ))
        project["status"] = "in_progress"

    async def get_progress(self, project_id: str):
        project = self.active_projects.get(project_id)
        if not project:
            return None
        total_agents = len(project["agents"])
        working = sum(1 for a in project["agents"] if a["status"] == "working")
        project["progress"] = min(100, int((working / total_agents) * 100))
        return project

